function hashCode(str) { // java String#hashCode
    let hash = 0
    for (let i = 0; i < str.length; i++) {
        hash = str.charCodeAt(i) + ((hash << 5) - hash)
    }
    return hash
}

function intToRGB(i) {
    const c = (i & 0x00FFFFFF)
        .toString(16)
        .toUpperCase();

    return "00000".substring(0, 6 - c.length) + c
}

const formatRelativeTime = (date) => {
    const rtf = new Intl.RelativeTimeFormat('en', { numeric: 'auto' });
    const now = new Date();
    const diffInSeconds = Math.floor((date - now) / 1000);
    const diffInMinutes = Math.floor(diffInSeconds / 60);
    const diffInHours = Math.floor(diffInMinutes / 60);
    const diffInDays = Math.floor(diffInHours / 24);

    if (Math.abs(diffInSeconds) < 60) return rtf.format(diffInSeconds, 'second');
    if (Math.abs(diffInMinutes) < 60) return rtf.format(diffInMinutes, 'minute');
    if (Math.abs(diffInHours) < 24) return rtf.format(diffInHours, 'hour');
    if (Math.abs(diffInDays) < 30) return rtf.format(diffInDays, 'day');
    if (Math.abs(diffInDays) < 365) return rtf.format(Math.floor(diffInDays / 30), 'month');
    return rtf.format(Math.floor(diffInDays / 365), 'year');
};

function getSuspendedURL(url) {
    const parsed_url = new URL(url)
    if (parsed_url.hostname == 'noogafoofpebimajpfpamcfhoaifemoa') {
        // is great suspender tab
        return new URL((new URLSearchParams(parsed_url.hash)).get('uri'))
    }
    return parsed_url
}

async function focusTabAndWindow(tabId) {
    // Get the tab information
    const tab = await chrome.tabs.get(Number(tabId));

    // Bring the window containing the tab to the foreground
    await chrome.windows.update(tab.windowId, { focused: true });

    // Focus the specific tab within the window
    const updatedTab = await chrome.tabs.update(Number(tabId), { active: true });
}

async function getTabsData() {
    const tabs = await chrome.tabs.query({});
    const tabData =
        tabs.map(tab => {
            const is_suspended = tab.url.includes('noogafoofpebimajpfpamcfhoaifemoa')
            const original_url = getSuspendedURL(tab.url)
            return {
                tabId: tab.id,
                url: original_url,
                title: tab.title,
                windowId: tab.windowId + ':' + tab.id,
                lastAccessed: tab.lastAccessed ? (new Date(tab.lastAccessed)) : null,
            };
        })
    return tabData;
}

