document.addEventListener('DOMContentLoaded', function () {
    chrome.tabs.query({}, function (tabs) {
        let domainGroups = { 'News': [] };
        for (const tab of tabs) {
            const url = new URL(tab.url)
            let domain = url.hostname;

            if (domain == 'noogafoofpebimajpfpamcfhoaifemoa') {
                // is great suspender tab
                const original_url = (new URLSearchParams(url.hash)).get('uri')
                domain = (new URL(original_url)).hostname
            }
            if (domain.startsWith('www.')) {
                // strip www. from front of domain
                domain = domain.substring(4)
            }

            domainGroups[domain] = domainGroups[domain] || [];
            domainGroups[domain].push(tab)

            if (newsDomains.has(domain.toLowerCase())) {
                domainGroups['News'].push(tab)
            }
        }

        // sort from most tabs per group to fewest
        const sorted_groups =
            Object.entries(domainGroups)
                .sort(([_a, tabsA], [_b, tabsB]) =>
                    tabsB.length - tabsA.length)

        const many_group = []
        const few_group = []
        const one_group = []

        // combine all groups that only have 1 tab into a single group
        const all_ungrouped = []
        for (const [domain, tabs] of Object.entries(domainGroups)) {
            if (tabs.length == 1) {
                all_ungrouped.push(...tabs);
                // delete domainGroups[domain]
            }
        }
        one_group.push(['Group all single tabs into one window...', all_ungrouped]);

        for (const [domain, tabs] of sorted_groups) {
            if (tabs.length > 4) {
                many_group.push([domain, tabs]);
            } else if (tabs.length > 1) {
                few_group.push([domain, tabs]);
            } else {
                one_group.push([domain, tabs]);
            }
        }

        const many = document.getElementById('tab-groups-many');
        const few = document.getElementById('tab-groups-few');
        const one = document.getElementById('tab-groups-one');

        for (const [groupname, group] of Object.entries({ many_group, few_group, one_group })) {
            for (const [domain, tabs] of group) {

                const button = document.createElement('button');
                button.textContent = `${domain} (${tabs.length})`
                button.style.border = '0px'
                button.style.backgroundColor = `#${intToRGB(hashCode(domain))}`
                button.style.color = 'white'
                button.style.textShadow = '1px 1px 1px rgba(0,0,0,0.2)'
                button.style.margin = '4px'
                button.style.borderRadius = '8px'

                if (domain == 'Group all single tabs into one window...') {
                    button.style.backgroundColor = 'black'
                    button.style.fontSize = '18px'
                }

                button.addEventListener('click', function () {
                    chrome.runtime.sendMessage({ action: "moveTabs", tabIds: tabs.map(tab => tab.id) });
                });

                const pocketButton = document.createElement('a');
                pocketButton.textContent = 'P'
                pocketButton.style.color = '#888'
                pocketButton.title = 'Save URLs to Pocket: ' + tabs.map(tab => tab.url).join(', ')
                pocketButton.style.margin = '3px';

                pocketButton.addEventListener('click', function () {
                    chrome.runtime.sendMessage({ action: "pocketURLs", tabIds: tabs.map(tab => tab.id) })
                })

                const closeButton = document.createElement('a');
                closeButton.textContent = 'x'
                closeButton.href = '#'
                closeButton.style.color = '#888'

                closeButton.addEventListener('click', function () {
                    chrome.runtime.sendMessage({ action: "closeTabs", tabIds: tabs.map(tab => tab.id) });
                    window.location.reload();
                });

                if (groupname == 'many_group') {
                    many.appendChild(button);
                    many.appendChild(pocketButton);
                    many.appendChild(closeButton)
                } else if (groupname == 'few_group') {
                    few.appendChild(button);
                    few.appendChild(pocketButton);
                    few.appendChild(closeButton)
                } else {
                    one.appendChild(button);
                    one.appendChild(pocketButton);
                    one.appendChild(closeButton)
                }
            }
        }
    });
});
